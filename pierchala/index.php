                        <div class="modal-header">
                            <h4>Functions of Novel Ret Isoforms in the Peripheral Nervous System</h4>    
                        </div>
                        <div class="modal-body">
                            <img src="images/project2.png" width="600px;" class="img-responsive">
                            <hr>
                            <p>Ret encodes a transmembrane receptor that is 20 exons long and encodes two known protein isoforms differing in C-terminal amino acid composition, referred to as RET9 and RET51.  Studies of human pheochromocytomas have identified three novel splice variants involving alternative splicing of the 5’-region of Ret. These splice variants involve the skipping of exon 3, exons 3 and 4, or exons 3, 4, and 5 and are referred to as RetΔ3, RetΔ34, and RetΔ345, respectively. Further analysis revealed that these 5’-Ret splice variants are also expressed in normal kidney tissue in both mice and humans, but their functional significance has not been established. The RETΔ3 and RETΔ345 isoforms are predicted to encode Ret proteins, but with deletions in the extracellular domain, while the RETΔ34 isoform is expected to encode a secreted fragment of Ret that is most likely subject to nonsense mediated decay.  Our RT-PCR analysis has shown that all three 5’-Ret splice variants are expressed in a wide array of tissues and at different ages in mice, and our preliminary experiments have confirmed the stable expression of RETΔ3 and RETΔ345 in transfected NIH/3T3 cells. We are continuing experiments to determine the signaling capacities and functions of these isoforms in vivo, particularly during development.</p>
