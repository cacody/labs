                                    <h3 class="karma" style="color:#bfbfbf;">Brian Pierchala, PhD</h3>
                                    <p><em>Principal Investigator</em></p>
                                    <p>Brian Pierchala received his B.S. in Biochemistry from Oakland University in Michigan and obtained a Ph.D. in Neuroscience from the Johns Hopkins University School of Medicine. In the laboratory of David D. Ginty, his doctoral research investigated the ability of Nerve Growth Factor, a potent survival factor for sensory and sympathetic neurons, to support neuronal function when only activating receptors on axon terminals. His work on “retrograde” NGF signaling influenced the most widely accepted view of the field, namely that stable ligand-receptor complexes are trafficked in neurons over long distances to regulate biochemical events in the cell body necessary for survival, growth, and differentiation. Dr. Pierchala conducted his postdoctoral training in the laboratory of Eugene M. Johnson, Jr. at Washington University School of Medicine in Saint Louis. There, he investigated a newly discovered family of neuronal growth factors, the Glial Cell Line-derived Neurotrophic Factor (GDNF) Family Ligands (GFLs) and made several contributions to the understanding of how the GDNF receptor complex signals survival and differentiation. He continued his investigation of GDNF receptor signal transduction as an Assistant Professor at the State University of New York at Buffalo prior to his arrival to the Department of Biologic and Materials Sciences. His laboratory investigates the mechanisms of action of neurotrophic factors and proapoptotic factors in the development, maintenance and regeneration of the peripheral nervous system.</p><p>Dr. Pierchala is a Principal Investigator on research projects funded by the NIH and CHDI.  He has instructed undergraduate students, Ph.D. graduate students and dental students in neurobiology, signal transduction, and cell biology. Dr. Pierchala serves as a reviewer for multiple journals including The Journal of Neuroscience, Nature and Science, and has served as a reviewer on NIH study sections.</p>
