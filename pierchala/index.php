                    <div class="modal-content" style="color:#000;">
                        <div class="modal-header">
                            <h4>A p75-Ret Signaling Complex Mediates Neuronal Survival and Death in the Developing Peripheral Nervous System</h4>    
                        </div>
                        <div class="modal-body">
                            <img src="images/project1.png" width="600px;" class="img-responsive">
                            <hr>
                            <p>In the PNS, GFLs signal through their common receptor tyrosine kinase, Ret, to produce signals critical for neuronal survival, migration, and axonal growth. Recently, our lab has discovered that GFL activation of Ret induces the association of Ret with p75NTR in sympathetic and sensory neurons. This interaction is surprising, as p75NTR is a member of the tumor necrosis factor (TNF) family of death receptors, and is a critical mediator of apoptosis in sympathetic neurons in the PNS. We have also found that Ret interacts with p75NTR upon brain-derived neurotrophic factor (BDNF) stimulation of neurons as well as NGF deprivation, both of which lead to apoptosis mediated by the p75NTR.  Moreover, we have identified a functional role of Ret in p75NTR pro-apoptotic signaling, and conversely, a functional role of the p75NTR in pro-growth/pro-survival Ret signaling. Thus, we propose that the newly identified Ret-p75 receptor complex has dual, opposing signals and functions, which depend upon which ligand promotes the assembly of this complex.  Current experiments are aimed at determining of the developmental functions of this p75-Ret complex and the mechanisms that dictate Ret-mediated apoptosis.</p>
