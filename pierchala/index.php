                            <h4>Ligand-receptor mechanisms responsible for development, maintenance and regeneration of the NMJ</h4>    
                        </div>
                        <div class="modal-body">
                            <img src="images/project3.jpg" width="600px;" class="img-responsive">
                            <hr>
                            <p>We are utilizing molecular genetic methods to determine which neurotrophic factors are responsible for the establishment of NMJs, and which competition factors are necessary for their refinement.  Functional reinnervation of the adult neuromuscular junction (NMJ) following injury is a highly regulated process that involves the coordinated effort of motor axon terminals, postsynaptic acetylcholine receptors (AChRs), and terminal Schwann cells (TSCs).  Utilizing both nerve crush and cardiotoxin injury paradigms, we are investigating how these same ligand/receptor complexes impact functional reinnervation of NMJs, with the long-term goal of elucidating mechanisms by which enhancement of muscle reinnervation can be achieved.</p>
