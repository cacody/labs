                    <div class="panel panel-default">
                        <div class="panel-body">
                            <h3 class="karma" style="color:#bfbfbf;">Pierchala Lab's research interests</h3>
                            <h4>Development and refinement of peripheral neural circuits</h4>
                            <p>During development of the nervous system, axonal projections and synaptic contacts are supported by target-derived neuroptrophic factors, such as the neurotrophins and the glial cell line-derived (GDNF) family ligands (GFLs).  During this period of target innervation, neurons often make excessive projections into their targets that are later pruned back.  Development of the neuromuscular junction (NMJ), for example, undergoes a process in which NMJs are initially innervated by several axons, known as polyneuronal innervation.  Then, through a process known as synaptic elimination, weaker connections are eliminated resulting in a 1:1 pairing between the presynaptic motor neuron and the postsynaptic receptor clusters in the muscle.  This competitive “pushing out” of weaker axonal connections occurs by the release of inhibitory factors by the successful axons.  The delicate balance between growth and survival-promoting neurotrophic factors, and inhibitory competitive factors, is thought to ultimately sculpt the architecture of mature circuits.  My laboratory is interested in understanding the ligand and receptor mechanisms responsible for growth and survival promotion, synaptic elimination and apoptosis, and regeneration of peripheral neurons.  We utilize biochemical and cell biological methods for the analysis of transgenic mice in which these receptors and ligands are deleted at specific developmental times.  The determination of these mechanisms of survival, cell death and circuit maintenance will enable a more rational approach for the development of therapeutic strategies for diseases and injuries of the nervous system.
</p>
