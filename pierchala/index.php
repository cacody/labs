            <div class="modal fade" id="project5" role="dialog">
                <div class="modal-dialog">
                    <div class="modal-content" style="color:#000;">
                        <div class="modal-header">
                            <h4>GDNF/Ret signaling complexes in the development of the peripheral taste system</h4>    
                        </div>
                        <div class="modal-body">
                            <img src="images/project5.jpg" width="600px;" class="img-responsive">
                            <hr>
                            <p>The lingual sense of taste is initially communicated by taste buds, clusters of sensory cells located in highly organized taste papillae, to sensory neurons whose cell bodies are located in the geniculate and petrosal ganglia.  The specialized chemosensory cells in taste buds continuously turn over and are replaced throughout life.  The molecular mechanisms responsible for the development and maintenance of taste buds are incompletely understood, but chemosensory innervation is critical for the maintenance of taste buds throughout the lifespan. The GFLs are expressed in the tongue during the period of chemosensory innervation, but it is not known whether gustatory chemosensory neurons require GFL/Ret signaling for survival, innervation or phenotypic maintenance.  Furthermore, the function of the GFL/Ret pathway in the development and maintenance of taste buds is unexplored.  We have recently discovered that Ret is expressed in a subpopulation of chemosensory neurons that innervate fungiform taste buds, and that this innervation is impaired in Ret-/- mice.  We are currently examining to what extent the GFL/Ret pathway in necessary for chemosensory innervation and maintenance of taste buds, and whether Ret has an early function in the development of chemosensory neurons.  These experiments may aid in the rational design of therapeutic interventions for diseases and injuries of peripheral sensory neurons, potentially including abnormal or impaired taste sensation, conditions known as dysgeusias and hypogeusias.</p>
