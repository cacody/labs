                    <div class="modal-content" style="color:#000;">
                        <div class="modal-header">
                            <h4>Role of p75 in the pathogenesis of Huntington’s Disease</h4>    
                        </div>
                        <div class="modal-body">
                            <img src="images/project4.jpg" class="img-responsive">
                            <hr>
                            <p>Huntington’s Disease (HD) is a dominantly inherited neurodegenerative disorder, currently affecting approximately 30,000 people in the US.  Despite the identification of the gene, huntingtin (HTT), responsible for HD in 1993, the pathogenesis of this disorder remains elusive.  Htt protein is expressed ubiquitously throughout the brain, and yet degeneration occurs more rapidly and more severely in specific brain regions, of which the striatum is one of the earliest affected.  Alterations in neurotrophin signaling have been proposed as a potential mechanism of striatal degeneration in HD.  Striatal medium spiney neurons are dependent on brain-derived neurotropic factor (BDNF) from the cortex for development and maintenance. Levels of BDNF and its high-affinity receptor, TrkB, are both reported to be misregulated in the striatum of HD-like animals as well as in post-mortem striata from human HD patients.  We are currently investigating whether germline and conditional p75 knockout mice are altered in their sensitivity to neurodegeneration in a mouse model of HD, and whether changes in p75 signaling coincide with HD pathogenesis.</p>
