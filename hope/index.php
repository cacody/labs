                        <h2 class="blue-text">ABOUT</h2>
                    <div class="panel panel-default">
                        <div class="panel-body">
                            <p>Directed by Dr. Alexandre F. DaSilva, H.O.P.E. (Headache &amp; Orofacial Pain Effort) is a multidisciplinary collaborative effort to investigate the brain as a research and therapeutic target for chronic trigeminal pain disorders, including primary headaches (e.g. migraine), TMJD and trigeminal neuropathic pain.</p><p>The fact that many therapeutic modalities for chronic pain, which focus on peripheral mechanisms, do not provide relief for treatment-resistant patients raises the possibility that the cause for the chronicity of these debilitating disorders may lie in the brain itself. One hypothesis is that functional and structural dysfunction of specific cortical areas (e.g. SI, DLPFC), even at molecular level (e.g. opioidergic and gabanergic mechanisms), may be responsible for the persistence and intensification of the pain suffering.</p><p>Together with collaborators from University of Michigan and other academic institutions, we use state-of-the-art neuroimaging techniques (fMRI, PET, MRS, DTI, and MRI-based morphometry) to study neuroplasticity, and to investigate novel therapeutic approaches and mechanisms (e.g. non-invasive brain stimulation) in chronic trigeminal pain disorders.</p>
