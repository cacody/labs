                                    <p>Dr. Alexandre DaSilva is an Assistant Professor at the Biologic &amp; Materials Sciences Department at the University of Michigan Dental School. He has received his Doctorate in Medical Science (DMSc) degree in Oral Biology with clinical training in trigeminal pain at Harvard University. His thesis subject was on somatotopic (fMRI) activation in the human trigeminal pain pathway. This training was followed by a post-doctoral fellowship on migraine neuroimaging at the Martinos Center for Biomedical Imaging, Massachusetts General Hospital, to investigate subcortical and cortical neuroplasticity in migraine patients. He was also an Instructor in the Psychiatric Department at Harvard University/McLean Hospital, as well as, an Assistant Clinical Investigator at the Forsyth Institute in Boston. During his training, he collaborated with his colleagues on innovative neuroimaging and non-invasive brain stimulation projects for chronic TMJD, trigeminal neuropathic pain and migraine.</p><p>He is currently the Director of H.O.P.E. (Headache &amp; Orofacial Pain Effort), which is a multidisciplinary collaborative effort to investigate the brain as a research and therapeutic target for chronic trigeminal pain disorders. The fact that many therapeutic modalities for chronic pain, which focus on peripheral mechanisms, do not provide relief for treatment-resistant patients raises the possibility that the cause for the chronicity of these debilitating disorders may lie in the brain itself. One hypothesis is that functional and structural dysfunction of specific cortical areas (e.g. SI, DLPFC), even at a molecular level (e.g. opioidergic and gabanergic mechanisms), may be responsible for the persistence and intensification of the pain suffering. Together with collaborators from the University of Michigan and other academic institutions, we use state-of-the-art neuroimaging techniques (fMRI, PET, MRS, DTI, and MRI-based morphometry) to study neuro