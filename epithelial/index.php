                            <h3 class="karma" style="color:#bfbfbf;">Research Interests</h3>
                            <p>Our laboratory focus on the understanding of the “histone code” in the context of disease progression (e.g., Periodontal Disease) and cancer biology. We explore the role of histone acetylation and chromatin remodeling in the control of epithelial functions including healing, maintenance of epithelial homeostasis and stem cell function. We also explore the epigenetic-driven mechanisms involved in tumor resistance to ionizing radiation and chemotherapy and focus on the use of “epi-drugs” to counteract cancer-driven histone modifications.</p>
