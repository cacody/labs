                </div>
                <div class="bottle">
                    <div class="container">
                        <div class="row" style="padding:50px;">
                            <div class="col-md-8 col-md-offset-2 large-text">
                                <p>The question that is driving our research in the long-term focuses on how we can apply stem cell-based therapies to repair damaged tissues.</p><p>We would like to determine how stem cells participate in the formation of branching organs and how they respond to various external and internal cues in normal, damaged or diseased environments. With this knowledge we then design ways to (re)generate new tissues and/or injured organs.</p>
                            </div>
                        </div>
                    </div>
                </div>
                <div class="bottle2">
                    <div class="container">
                        <div class="row" style="padding:50px;">
                            <div class="col-md-8 col-md-offset-2 large-text">
                                <p>A particular area of the lab is focused on patients suffering from saliva loss, termed hypo-salivation, which can lead to dry mouth syndrome or xerostomia. This often painful and devastating oral health condition can occur in head-and-neck cancer patients receiving radiation on their salivary glands as part of radiotherapy treatment, or as a result of auto-immune diseases like Sjögren, or from the intake of certain medications.</p><p>With respect to those patients, we aim to team up with other experts in the field to define what is biologically and molecularly occurring in the damaged tissue, and define regenerative therapies that can be translated into the clinic. 
        </p>
                            </div>
                        </div>
