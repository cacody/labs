                            <h3 class="karma" style="color:#bfbfbf;">Taichman Lab's research interests</h3>
                            <h4>Overview</h4>
                            <p>Our laboratory works at the intersection of how hematopoietic stem cells (HSCs), mesenchymal stem cells (MSCs) and disseminated tumor cells (DTCs) home, localize and engage the bone marrow microenvironment or niche for survival, function and protection from injury. We explore the paracrine, autocrine, intracrine and cell-matrix and adhesive interactions which regulate stem cell function. We have taken a broad systems approach to understanding the normal physiology of the marrow in terms of cytokine and hormonal regulation of stem cell function including the identification of unique stem cell properties of normal resident stem cells and DTCs.</p>
                            <div class="row">
                                <div class="col-md-6">
                                    <img src="images/About%20Image.png">    
                                </div>
                                <div class="col-md-6">
                                    <img src="images/inhood-sm.jpg">
                                </div>
                            </div>
                            <h4>Areas of focus</h4>
                            <p><strong>HSC Niche Investigations</strong><br>Hematopoietic stem cell differentiation occurs in proximity to osteoblasts with in the bone marrow cavity. In 1992 we were the very first to hypothesize that this intimate physical association established early in life is to facilitate interactions between bone and hematopoietic cells.  To test this hypothesis we explored the ability of human osteoblasts to serve as a source of essential hematopoietic growth factors and adhesion
molecules to serve as a ‘niche’ for the localization and support hematopoietic stem cells. In 2003 this field
exploded starting several reports in high profile journals suggesting that indeed osteoblasts were critical niche contributors to HSC maintenance. Shortly thereafter and continuing to this day are reports that suggest that other members of the bone marrow niche either contribute or take priority to osteoblasts in terms of HSC support. The majority of the newer work has been contributed using genetic and animal models. Our work continues to remain the foundation of this often-debated area and continues to among the only data available in the human system</p>
                            <p><strong>Metastatic Tumor Cells Parasitize the HSC Niche</strong><br>The dissemination of prostate cancer (<strong>PCa</strong>) cells by a primary tumor into the circulation occurs early in the disease process. In fact, disseminated tumor cells (<strong>DTCs</strong>) have been identified in the bone marrow of majority of PCa patients at the time of primary diagnosis2. While most DTCs do not develop into metastases, many do leading to morbidity and death. Predicting whether an individual will suffer a clinical recurrence following primary treatment remains a substantial challenge and relies purely on broad clinical and pathological criteria. Understanding how DTCs function will transform our ability treat metastases.  In 2002 we were only the second team to publish that SDF-1/CXCL12 pathway regulates metastasis, and from there we have gone on to demonstrate that circulating PCa cells target the ‘niche,’ in marrow that houses HSCs. We demonstrated that (i) DTCs co-localize with HSCs in marrow, (ii) DTCs use the same receptors as HSCs to localize to the niche, and, critically, (iii) in competition studies DTCs compete for occupancy of the niche with HSCs. In ongoing investigations we explore the niche mechanisms used to regulate quiescence of DTCs and how “niche-engaged” DTCs acquire a cancer stem cell phenotype.</p>
                            <p><strong>Tissue Regeneration Investigations Using Marrow Derived Stem-Like Cells</strong><br>Our group is interested in developing therapies for the regeneration of craniofacial injuries or conditions which will require the development of multiple tissue components   Previously, we demonstrated that a significant proportion of the osseous regenerative capacity resides in a low density cellular fraction which is resistant to agents that induce apoptosis of cells actively undergoing DNA synthesis.  FACS analysis further identified very small cells which do not express CD45 or other hematopoietic lineage markers (Lin-), and in mouse marrow expresses the Sca-1 antigen.  These small, CXCL12-responsive, Lin-Sca-1+CD45- cells had previously been described as having embryonic-like features.  Therefore, the cells were described as very small embryonic-like (<strong>VSEL</strong>) cells representing less than 0.02% of nucleated cells in marrow.  VSEL cells express genes that are expressed by embryonic stem cells (Oct4, nanog, SSEA-1). Our work is aimed at characterizing these unique cells to lay the foundation for future therapeutic investigator initiated trials.  </p>
                            <p><strong>Hormonal regulation of tissue interfaces</strong><br>The effect of erythropoietin (Epo) on non-hematopoietic tissues remains controversial.  Recent studies have demonstrated that Epo has extensive non-hematopoietic biological functions. We have explored the role of Epo in bone physiology and have noted that high levels of Epo and locally delivered Epo activates (i) HSCs to secrete BMPs that activate MSCs to produce bone and (i) Epo inhibits osteoclastic bone formation. Our studies suggest that Epo or Epo-like agents may represent a therapeutic strategy for tissue regeneration and continue to explore these possibilities with knowledge the anabolic activity of Epo is likely to be context and tissue dependent.</p>
                            <p><strong>Academic Leadership</strong><br>Dramatic changes in the scope and mode of delivering health care are on the horizon.  Therefore a strategic approach to address the emerging opportunities and challenges is required. This  will necessitate new and sustained leadership initiatives to position the profession with the skills, knowledge and passion guide oral health care into the future.</p>
