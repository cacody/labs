                                <div class="col-md-10">
                                    <p><em>William K. and Mary Anne Najjar Professor<br>Dean, U-M School of Dentistry</em><br><a href="files/CV_McCauleyOctober2015.pdf">Curriculum Vitae</a></p>
                                    <p>Laurie K. McCauley is the William K. and Mary Anne Najjar Professor and Dean of the School of Dentistry, and Professor in the Department of Pathology at the Medical School at the University of Michigan. Dr. McCauley earned her B.S., D.D.S., M.S. and Ph.D. (Veterinary Pathobiology) all from The Ohio State University. She has had several visiting scientist/professor appointments including the Institut de Genetique et de Biologie Moleculaire et Cellulaire, the École Normale Supérieure de Lyon, and the Center for Experimental Therapeutics and Reperfusion Injury, Brigham and Women’s Hospital, Harvard Medical School.</p><p>Dr. McCauley is a diplomate of the American Board of Periodontology, a fellow in the American Association for the Advancement of Science, a former council member of the American Society for Bone and Mineral Research (ASBMR), former Associate Editor of the Journal of Bone and Mineral Research (JBMR), a Fellow in the American College of Dentists and the International College of Dentists, and also served on the National Institutes of Health, National Advisory Dental &amp; Craniofacial Research Council. For more than twenty years, Dr. McCauley has led an active research program in hormonal controls of bone remodeling, parathyroid hormone anabolic actions in bone, and prostate cancer skeletal metastasis. Among her many recognitions are the inaugural Paula Stern Achievement award from the ASBMR, a distinguished scientist award from the International Association for Dental Research, The Ohio State College of Dentistry Distinguished Alumnus award, and membership in the National Academy of Medicine.</p>
