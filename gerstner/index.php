                    <h2 class="blue-text">RESEARCH</h2>                  
                    <div class="panel panel-default">
                        <div class="panel-body">
                            <p>Mammalian chewing is wonderfully diverse in dentoskeletal morphological forms as well as in kinematic forms both within and between species. However, the chewing rhythm is surprisingly invariant within species and the rhythm scales with body mass across species. I am interested in understanding how and why mammalian brains, which are connected to neuromuscular and dentoskeletal systems, and ultimately connected to feeding environments and developmental processes, produce the diversity of kinematic forms in conjunction with fixed rhythms. Presently, we are addressing these issues through cross-species comparative kinematic studies and using a number of sophisticated statistical methods (functional data analysis, geometric morphometrics). The questions of interest (in Tinbergen terms) are: (1) Why do certain animals chew rhythmically and others do not? (2) What functional roles does a relatively fixed rhythmicity play, i.e., does it increase an animal’s fitness and if so how and why? (3) How does the rhythm develop and are there developmental windows during which times brainstem circuits are adaptable to feedback? (4) What neural and non-neural mechanisms play a role in “fixing” the rhythm at specific rates?</p>
