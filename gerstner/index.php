                                <div class="col-md-10">
                                    <h3 class="panel-title">Geoffrey Gerstner, DDS, MS, PhD</h3>
                                    <em>Principal Investigator</em><br><a href="mailto:geger@umich.edu">geger@umich.edu</a><br>
                                    <p style="padding-top:20px">Dr. Gerstner holds a BS in Zoology from BYU, where he became interested in animal behavior and mammalogy as a pre-dental student. After graduating, he enrolled in a dual degree program at UCLA where he continued animal behavior studies in an MS program while obtaining a DDS degree. As an MS student, he studied motor control in a guinea pig model of tardive dyskinesia and became interested in the nature of time and temporality in motor behavior. After completing the DDS and MS, he pursued further graduate training through an individual NIH Dentist Scientist Award. He studied chronic pain management as the clinical component of this training and as the research component, he obtained a PhD in Neuroscience, during which time he did comparative behavior studies of giraffes (Giraffa camelopardalis), okapis (Okapia johnstoni), roe deer (Capreolus capreolus), gray kangaroos (Macropus giganteus), red pandas (Ailurus fulgens) and raccoons (Procyon lotor) at the San Diego Zoo, San Diego Wild Animal Park, Los Angeles Zoo and Phoenix Zoo. His present work continues to push the tension between clinical and field-biology.</p>
                                    <p>Dr. Gerstner’s research focuses on feeding motor behaviors in mammals, including humans. Respiration and swallowing are behaviors that cannot occur simultaneously. Darwin states in The Origin of the Species “the strange fact that every particle of food and drink which we swallow has to pass over the orifice of the trachea, with some risk of falling into the lungs, notwithstanding the beautiful contrivance by which the glottis is closed”. The “beautiful contrivance” involves complex motor behaviors that shunt air and food into the appropriate respiratory or digestive systems caudal to the throat. Due to mammalian endothermic requirements, the blockage of air flow that occurs during swallowing must be brief. This brevity is enhanced by mastication, a mammal-specific way of efficiently reducing food intra-orally. Because the mammalian mouth is separated from the nasal cavity by a secondary palate, respiration and mastication can co-occur. But mastication causes tooth wear, and excessive tooth wear can reduce fitness. Mammals possess unique mouths, jaws and teeth and advanced motor control systems so that viable tradeoffs between food particle reduction and tooth wear can occur in mammals, and thus allow the incomplete separation of respiratory and digestive systems at the level of the oropharynx to remain a viable ‘design flaw’. A testament to the success of mammals is the breathtaking variety of jaw and tooth morphology and feeding niche diversity that exists within this vertebrate class. The mammalian radiations have been accompanied by rich and diverse feeding motor behaviors involving jaw, palate, tongue, and pharyngeal coordination necessary to handle foods efficiently. But, because the system is not ‘perfectly designed’, there can be numerous clinical conditions that arise including excessive tooth wear from overuse or dietary sources, feeding disorders in children, cleft palates, swallowing disorders, etc. Dr. Gerstner collaborates with other experts to study brain mechanisms of mastication, the significance of tooth wear, and clinical conditions wherein the flaws of the mammalian ingestive system become manifest. Data are collected in clinical, field and museum settings, and used to understand the relationships and interactions between chewing rhythmicity, jaw kinematics, oral morphology, phylogeny, feeding niche and clinical disorders. </p>
