                            <p>This work is done in the laboratory and in the Graduate Implant Clinic at the University of Michigan. We are studying night-time bruxing and tooth wear while evaluating a new mouth guard that may help reduce costs of dental care for bruxers. Bruxism increases tooth wear and we are monitoring the rate of tooth wear in bruxers, whether rates are predictable, and whether rates are significantly reduced with mouth guard use. We are also looking at how tooth wear impacts masticatory performance and other dental health factors. The work is funded by the Delta Dental Foundation of Michigan.</p>
